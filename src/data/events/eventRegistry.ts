export type PageType = 'auction' | 'prize-draw' | 'redeem' | 'standard' | 'waitlist';

export type EventCategory =
  | 'Concerts and festivals'
  | 'Sport and leisure'
  | 'Shows and culture'
  | 'Food and drinks'
  | 'Wellness'
  | 'Hotel experiences'
  | 'Visits'
  | 'Tech';

export interface HeroImage {
  src: string;
  alt: string;
}

export type MarketingTagType = 'presale' | 'exclusivity' | 'sold-out' | 'discount';

export interface EventData {
  id: string;
  title: string;
  description: string;
  date: string;
  location: string;
  city: string;
  pageType: PageType;
  category: EventCategory;
  points: number;
  image: string;
  heroImages: HeroImage[];
  includedItems: string[];
  eventTag?: string;
  venue?: string;
  msLeft?: number;
  currentBid?: number;
  bidChips?: number[];
  ticketPrice?: number;
  maxTickets?: number;
  /** Prize draw end date (ISO string). When set, used instead of msLeft for draw end. */
  drawEndDate?: string;
  imagePosition?: string;
  marketingTag?: MarketingTagType;
}

/** Event is Voyager-exclusive when it has Presale or Exclusivity marketing tag. */
export function isVoyagerExclusiveEvent(event: EventData | null | undefined): boolean {
  if (!event?.marketingTag) return false;
  return event.marketingTag === 'presale' || event.marketingTag === 'exclusivity';
}

const ROUTE_MAP: Record<PageType, string> = {
  'auction': '#auction',
  'prize-draw': '#draw',
  'redeem': '#redeem',
  'standard': '#standard',
  'waitlist': '#waitlist',
};

export function getEventRoute(event: EventData): string {
  return `${ROUTE_MAP[event.pageType]}/${event.id}`;
}

export function getPaymentLabel(pageType: PageType): string {
  switch (pageType) {
    case 'auction': return 'Current bid';
    case 'prize-draw': return 'Prize Draw';
    case 'redeem': return 'Redeem';
    case 'standard': return 'Cash';
    case 'waitlist': return 'Waitlist';
  }
}

export function formatPoints(n: number): string {
  return n.toLocaleString('de-DE');
}

/** Points barrier for ordering / “can I afford this?” (prototype heuristic). */
export function getEffectivePointsCost(event: EventData): number {
  switch (event.pageType) {
    case 'auction':
      return event.currentBid ?? event.points;
    case 'prize-draw':
      return event.ticketPrice ?? event.points;
    case 'redeem':
      return event.points;
    case 'standard':
    case 'waitlist':
      return 0;
    default:
      return event.points;
  }
}

/** Whether the user’s points balance is enough for this mechanic (approximate). */
export function isEventAffordableWithBalance(event: EventData, userPoints: number): boolean {
  if (event.pageType === 'standard' || event.pageType === 'waitlist') return true;
  const cost = getEffectivePointsCost(event);
  if (event.pageType === 'auction') return userPoints > cost;
  return userPoints >= cost;
}

