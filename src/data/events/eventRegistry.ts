export function getEventById(id: string): EventData | undefined {
  return EVENT_REGISTRY.find((e) => e.id === id);
}

export function getEventsByCity(city: string): EventData[] {
  return EVENT_REGISTRY.filter((e) => e.city === city);
}

export function getEventsByCategory(category: string): EventData[] {
  return EVENT_REGISTRY.filter((e) => e.category === category);
}

export function getEventsByPageType(pageType: PageType): EventData[] {
  return EVENT_REGISTRY.filter((e) => e.pageType === pageType);
}

export function getParisEvents(): EventData[] {
  return EVENT_REGISTRY.filter((e) => e.city === 'Paris');
}

export function getFrenchCityEvents(): EventData[] {
  return EVENT_REGISTRY.filter((e) => e.city !== 'Paris');
}
